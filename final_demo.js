        'time': ['–≤—Ä–µ–º—è', '—Å—Ä–æ–∫', '–¥–æ–ª–≥–æ', '–±—ã—Å—Ç—Ä–æ', '–∫–æ–≥–¥–∞', '–¥–ª–∏—Ç–µ–ª—å–Ω–æ—Å—Ç—å', '–æ–∂–∏–¥–∞–Ω–∏–µ'],
        'order': ['–∑–∞–∫–∞–∑', '–∫—É–ø–∏—Ç—å', '–ø–æ–ª—É—á–∏—Ç—å', '–æ–∂–∏–¥–∞—Ç—å', '–∑–∞–∫–∞–∑–∞—Ç—å', '–º–æ–π –∑–∞–∫–∞–∑', '–æ—Ñ–æ—Ä–º–∏—Ç—å'],
